        <h1>Brandon Christopher on <em>The Job Pirate</em></h1>
        <p><img alt="Brandon Christopher" class="right" style="width:250px" src="/imgs/authors/Author-Brandon-Christopher-portrait1.jpg">You know, when I first started writing <em><a href="http://www.amazon.com/Job-Pirate-Entertaining-Job-hopping-Journey/dp/0990573206/ref=sr_1_1?ie=UTF8&amp;qid=1412072372&amp;sr=8-1&amp;keywords=the+job+pirate" target="_blank">The Job Pirate</a></em>, it was simply a way for me to document little snippets
            of my life at shitty jobs. It was strictly humor. But as the years went on, and I saw how screwed up this
            country has become, and how banks and oil companies started running everything, I realized there was a lot
            more to talk about. The Job Pirate then became like a worker’s survival guide for scoring a decent job with
            health insurance in this strange, new 21st Century. My own Generation X may be screwed, but the Millennial
            Generation is absolutely fucked. You have to acclimate or it’s adios time, friends. Be whatever you want to
            be, as many times as you want. That’s what being a Job Pirate is all about.</p>

        <p>I knew I wanted to be a writer at the age of 15 after reading Hemingway and Salinger in school. Writers
            seemed to be the patron saints of outsiders and outcasts, and I was definitely a pariah in high school. The
            first thing I attempted to write was a script for the short-lived TV show “Sledgehammer,” then some terrible
            fiction stories followed. But then I discovered Charles Bukowski a year or so later, and I found a whole new
            literary world to explore—a world where the writer was the protagonist of his own stories, and the plot is
            simply life itself.</p>
