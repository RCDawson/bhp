        <h1>Aida Zilelian on <em>Legacy of Lost Things</em></h1>
        <p><img alt="Aida Zilelian" class="right" style="max-width:250px;border:1px solid #efefef; margin-bottom:1.66667em" src="/imgs/authors/Author-Aida-Zilelian-portrait1.jpg"></p>

        <p>For years I resented being Armenian. My parents were extremely active in the community and it was quite an
            insular existence. I went to an Armenian elementary school that my father had helped established, as well as
            Armenian Saturday school and Sunday school at an Armenian church. Once I was old enough to completely remove
            myself from the community I did, and for years. It didnâ€™t occur to me until much later that being Armenian
            was visceral. It crept into the melodies of the music I wrote, the music I listened to, the words that I
            eventually put on paper. It was inescapable and I grew to respect the reality of that. And it moved me to
            give it the honor it deserved.</p>
